days = input("Enter a day you would like to translate from english to swedish: ")

if days == "Monday":
    print("Monday gets translated to måndag")
elif days == "Tuesday":
    print("Tuesday gets translated to tisdag")
elif days == "Wednesday":
    print("Wednesday gets translated to onsdag")
elif days == "Thursday":
    print("Thursday gets translated to torsdag")
elif days == "Friday":
    print("Friday gets translated to fredag")
elif days == "Saturday":
    print("Saturday gets translated to lördag")
elif days == "Sunday": 
    print("Sunday gets translated to söndag")
else: 
    print("Input incorrect, please try again")