package lab3.com.Shape;

/**
 * An interface representing shapes
 */
public interface Shape {
    /**
     * Calculates the area of the shape
     * @return The area of the shape as a double
     */
    public double area();

    /**
     * Calculates the circumference of the shape
     * @return The circumference of the shape as a double
     */
    public double circumference();
    
}
